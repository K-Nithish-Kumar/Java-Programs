class forLoop {
    public static void main(String args[]) {
        int n = 5;
        //for loop to print 5 numbers from 0 to 5
        for(int i = 0; i <= n; i++) {
           System.out.println(i);
        }
        //for loop to print 5 numbers from 5 to 0
        for(int i = n; i >= 0 ; i--) {
           System.out.println(i);
        }
    }
}